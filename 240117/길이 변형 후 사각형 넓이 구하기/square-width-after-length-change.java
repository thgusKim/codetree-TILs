import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // 변수 선언
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        // 출력
        System.out.println(a+8);
        System.out.println(b*3);
        System.out.println(a*b);
    }
}